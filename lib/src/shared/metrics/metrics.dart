/// Classe que centraliza os valores de espaçamento e tamanhos usados no app.
/// Facilita ajustes visuais e mantém consistência entre telas e componentes.
class AppMetrics {
  // Espaçamentos padrão
  static const double paddingSmall = 8.0;
  static const double paddingMedium = 16.0;
  static const double paddingLarge = 24.0;

  // Tamanhos de fontes
  static const double fontSmall = 12.0;
  static const double fontMedium = 16.0;
  static const double fontLarge = 20.0;

  // Altura padrão de botões
  static const double buttonHeight = 48.0;

  // Espaçamento entre componentes
  static const double componentSpacing = 12.0;
}